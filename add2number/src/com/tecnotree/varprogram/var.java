package com.tecnotree.varprogram;
class Main 
{
  public static void main(String[] args) 
  {
    // create variables
    int a = 15;
    int var;

    // assign value using =
    var = a;
    System.out.println("var using =: " + var);

    // assign value using =+
    var += a;
    System.out.println("var using +=: " + var);

    // assign value using =*
    var *= a;
    System.out.println("var using *=: " + var);
  }
}
